    private void btnSalirGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalirGuardarActionPerformed
        //Se guardan los datos del sistema en el fichero
        FileOutputStream controladoraArchivo;
        ObjectOutputStream controladoraWrite;
        
        try {
            controladoraArchivo = new FileOutputStream("src/Archivo/datos.dat");
            controladoraWrite = new ObjectOutputStream(controladoraArchivo);
            controladoraWrite.writeObject(Controladora.getInstance());
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, e);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        dispose();
    }//GEN-LAST:event_btnSalirGuardarActionPerformed

