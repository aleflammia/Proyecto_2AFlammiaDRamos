    private void btnBuscarArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarArticuloActionPerformed
        String nombre = JOptionPane.showInputDialog(null, "Ingrese el nombre del articulo", "Buscar Articulo", JOptionPane.OK_OPTION);
        Articulo articulo = null;
        if (nombre != null) {
            articulo = Controladora.getInstance().getHashTable().buscarArticulo(nombre);
        }
        if (articulo != null && !nombre.equalsIgnoreCase("")) {
            Detalles detalles = new Detalles(this, true, articulo);
            //JOptionPane.showMessageDialog(null, Controladora.getInstance().previsualizar(articulo));
            detalles.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "No se pudo encontrar el Articulo");
        }
    }//GEN-LAST:event_btnBuscarArticuloActionPerformed

